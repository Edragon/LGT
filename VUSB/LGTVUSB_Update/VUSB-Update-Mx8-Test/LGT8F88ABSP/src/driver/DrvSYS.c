void DrvSYS_EnableSMOD(ENUM_SLEEP_MOD eSMod)
{
	u8 u8Mod;
	u8Mod = ((eSMod & 0x7) << 1) | 0x1;
	SMCR = u8Mod;
}

void DrvSYS_DisableSMOD(void)
{
	SMCR = 0;
}

